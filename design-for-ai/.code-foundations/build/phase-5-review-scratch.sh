#!/usr/bin/env bash
# Phase 5 review scratch — desk-check greps for mock.md and build.md
set -euo pipefail
MOCK="/Users/r/repos/design-for-ai/commands/mock.md"
BUILD="/Users/r/repos/design-for-ai/commands/build.md"
AGENTS_DIR="/Users/r/repos/design-for-ai/agents"

echo "========================================================"
echo "EXISTENCE CHECKS"
echo "========================================================"
echo -n "commands/mock.md exists: "
[ -f "$MOCK" ] && echo "YES" || echo "NO — FAIL"
echo -n "commands/build.md exists: "
[ -f "$BUILD" ] && echo "YES" || echo "NO — FAIL"
echo -n "agents/design-build-agent.md exists: "
[ -f "$AGENTS_DIR/design-build-agent.md" ] && echo "YES" || echo "NO — FAIL"
echo -n "agents/design-review-agent.md exists: "
[ -f "$AGENTS_DIR/design-review-agent.md" ] && echo "YES" || echo "NO — FAIL"

echo ""
echo "========================================================"
echo "FRONTMATTER CHECKS"
echo "========================================================"
echo "--- mock.md frontmatter ---"
head -5 "$MOCK"
echo ""
echo "--- build.md frontmatter ---"
head -5 "$BUILD"

echo ""
echo "========================================================"
echo "DW-5.1: mock.md — agent dispatches"
echo "========================================================"
echo "--- design-build-agent dispatch ---"
grep -n "design-build-agent" "$MOCK" || echo "NOT FOUND — FAIL"
echo "--- design-review-agent dispatch ---"
grep -n "design-review-agent" "$MOCK" || echo "NOT FOUND — FAIL"
echo "--- prototype skill routing ---"
grep -n "prototype" "$MOCK" || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.1: mock.md — screenshot + findings presentation"
echo "========================================================"
echo "--- screenshot presentation ---"
grep -n -i "screenshot" "$MOCK" || echo "NOT FOUND — FAIL"
echo "--- findings presentation ---"
grep -n -i "findings" "$MOCK" || echo "NOT FOUND — FAIL"
echo "--- severity-ranked findings ---"
grep -n -i "severity\|Critical\|Major\|Minor" "$MOCK" || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.1 / DW-5.4: mock.md — sign-off gate"
echo "========================================================"
grep -n -i "sign.off\|gate\|approve\|reject\|MANDATORY\|explicit" "$MOCK" || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.1 / DW-5.4: mock.md — chain to build on approve / loop to plan on reject"
echo "========================================================"
echo "--- approve → build branch ---"
grep -n -i "approve\|build" "$MOCK" | grep -i "build\|proceed" || echo "NOT FOUND — FAIL"
echo "--- reject → plan loop ---"
grep -n -i "reject\|loop\|plan\|adjust" "$MOCK" | grep -v "^Binary" || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.2: build.md — worktree gate"
echo "========================================================"
echo "--- worktree gate mention ---"
grep -n -i "worktree\|Worktree Gate" "$BUILD" || echo "NOT FOUND — FAIL"
echo "--- main/master blocking ---"
grep -n -i "main\|master" "$BUILD" | head -20 || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.2: build.md — BUILD→REVIEW→commit loop"
echo "========================================================"
grep -n -i "BUILD.*REVIEW\|N\.1.*BUILD\|N\.2.*REVIEW\|Sub-Phase" "$BUILD" || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.2: build.md — gate resolution Full/Standard/Minimal"
echo "========================================================"
grep -n -i "Full\|Standard\|Minimal" "$BUILD" | head -20 || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.2: build.md — execution log"
echo "========================================================"
grep -n -i "execution.log\|execution log" "$BUILD" || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.2: build.md — trust report"
echo "========================================================"
grep -n -i "trust report\|REPORT" "$BUILD" | head -10 || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.3: both commands dispatch BY NAME (subagent_type)"
echo "========================================================"
echo "--- mock.md subagent_type ---"
grep -n "subagent_type" "$MOCK" || echo "NOT FOUND — FAIL"
echo "--- build.md subagent_type ---"
grep -n "subagent_type" "$BUILD" || echo "NOT FOUND — FAIL"
echo "--- mock.md prototype skill routing (not inlined) ---"
grep -n "prototype" "$MOCK" | grep -v "^Binary" || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.4: mock.md — explicit blocking gate (no silent proceed)"
echo "========================================================"
grep -n -i "MANDATORY\|never proceed\|without.*explicit\|no silent\|loop back" "$MOCK" || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "DW-5.4: build.md — worktree gate is NON-NEGOTIABLE"
echo "========================================================"
grep -n -i "NON-NEGOTIABLE\|never proceed.*main\|never.*main\|Block" "$BUILD" | head -10 || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "EDGE CASE: mock with no DESIGN.md → wireframe mode + flag"
echo "========================================================"
grep -n -i "wireframe\|DESIGN.md absent\|Design isn.*locked\|not locked" "$MOCK" || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "EDGE CASE: build on main → worktree gate blocks"
echo "========================================================"
grep -n -i "main.*block\|Block.*main\|main.*master.*Block\|never.*main\|main.*ask\|on main" "$BUILD" | head -10 || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "EDGE CASE: user rejects mock → loops to plan, no proceed to build"
echo "========================================================"
grep -n -i "reject\|loop.*plan\|plan.*loop\|not.*proceed\|no.*proceed\|looping to plan" "$MOCK" || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "INTERNAL CONSISTENCY: debiasing rule in both review dispatches"
echo "========================================================"
echo "--- mock.md debiasing ---"
grep -n -i "intent.framing\|no plan\|framing\|Debiasing\|NO.*intent" "$MOCK" | head -10 || echo "NOT FOUND — FAIL"
echo "--- build.md debiasing ---"
grep -n -i "intent.framing\|Debiasing\|NO.*intent\|no.*Context\|no.*framing" "$BUILD" | head -10 || echo "NOT FOUND — FAIL"

echo ""
echo "========================================================"
echo "ALL CHECKS COMPLETE"
echo "========================================================"
